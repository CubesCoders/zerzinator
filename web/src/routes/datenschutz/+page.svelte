				Adressen und E-Mail-Adressen, sowie Kommunikationsmittel wie soziale Medien-Handles und
				Instant-Messaging-Identifikatoren.
			</li>
			<li class="pb-2">
				Meta-, Kommunikations- und Verfahrensdaten: Meta-, Kommunikations- und Verfahrensdaten sind
				Kategorien, die Informationen über die Art und Weise enthalten, wie Daten verarbeitet,
				übermittelt und verwaltet werden. Meta-Daten, auch bekannt als Daten über Daten, umfassen
				Informationen, die den Kontext, die Herkunft und die Struktur anderer Daten beschreiben. Sie
				können Angaben zur Dateigröße, dem Erstellungsdatum, dem Autor eines Dokuments und den
				Änderungshistorien beinhalten. Kommunikationsdaten erfassen den Austausch von Informationen
				zwischen Nutzern über verschiedene Kanäle, wie E-Mail-Verkehr, Anrufprotokolle, Nachrichten
				in sozialen Netzwerken und Chat-Verläufe, inklusive der beteiligten Personen, Zeitstempel
				und Übertragungswege. Verfahrensdaten beschreiben die Prozesse und Abläufe innerhalb von
				Systemen oder Organisationen, einschließlich Workflow-Dokumentationen, Protokolle von
				Transaktionen und Aktivitäten, sowie Audit-Logs, die zur Nachverfolgung und Überprüfung von
				Vorgängen verwendet werden.
			</li>
			<li class="pb-2">
				Nutzungsdaten: Nutzungsdaten beziehen sich auf Informationen, die erfassen, wie Nutzer mit
				digitalen Produkten, Dienstleistungen oder Plattformen interagieren. Diese Daten umfassen
				eine breite Palette von Informationen, die aufzeigen, wie Nutzer Anwendungen nutzen, welche
				Funktionen sie bevorzugen, wie lange sie auf bestimmten Seiten verweilen und über welche
				Pfade sie durch eine Anwendung navigieren. Nutzungsdaten können auch die Häufigkeit der
				Nutzung, Zeitstempel von Aktivitäten, IP-Adressen, Geräteinformationen und Standortdaten
				einschließen. Sie sind besonders wertvoll für die Analyse des Nutzerverhaltens, die
				Optimierung von Benutzererfahrungen, das Personalisieren von Inhalten und das Verbessern von
				Produkten oder Dienstleistungen. Darüber hinaus spielen Nutzungsdaten eine entscheidende
				Rolle beim Erkennen von Trends, Vorlieben und möglichen Problembereichen innerhalb digitaler
				Angebote
			</li>
			<li class="pb-2">
				Personenbezogene Daten: "Personenbezogene Daten" sind alle Informationen, die sich auf eine
				identifizierte oder identifizierbare natürliche Person (im Folgenden "betroffene Person")
				beziehen; als identifizierbar wird eine natürliche Person angesehen, die direkt oder
				indirekt, insbesondere mittels Zuordnung zu einer Kennung wie einem Namen, zu einer
				Kennnummer, zu Standortdaten, zu einer Online-Kennung (z. B. Cookie) oder zu einem oder
				mehreren besonderen Merkmalen identifiziert werden kann, die Ausdruck der physischen,
				physiologischen, genetischen, psychischen, wirtschaftlichen, kulturellen oder sozialen
				Identität dieser natürlichen Person sind.
			</li>
			<li class="pb-2">
				Verantwortlicher: Als "Verantwortlicher" wird die natürliche oder juristische Person,
				Behörde, Einrichtung oder andere Stelle, die allein oder gemeinsam mit anderen über die
				Zwecke und Mittel der Verarbeitung von personenbezogenen Daten entscheidet, bezeichnet.
			</li>
			<li class="pb-2">
				Verarbeitung: "Verarbeitung" ist jeder mit oder ohne Hilfe automatisierter Verfahren
				ausgeführte Vorgang oder jede solche Vorgangsreihe im Zusammenhang mit personenbezogenen
				Daten. Der Begriff reicht weit und umfasst praktisch jeden Umgang mit Daten, sei es das
				Erheben, das Auswerten, das Speichern, das Übermitteln oder das Löschen.
			</li>
		</ul>
